static const  double eps = 0.001;

double phi(double x, double alpha = -1)
{
	return sqrt(x * alpha + alpha * log(x));;
}

int main()      
{
	double x0 = 0.5 , x1 = phi(x0), d = 1;
	int k = 1;
	cout << "epsilon1 = " << eps << endl
		<< "x(0) = " << x0 << endl << "-------------------------------\n\n";

	while (d > eps)
	{
		x1 = phi(x0);
		d = fabs(x1 - x0);
		x0 = x1;
		cout << "x" << "(" << k << ") = " << x1 << setw(10) << "| " << "x" << "(" << k << ") - x(" << (k - 1) << ") | = " << d << endl << endl;
		k++;
	}
	return 0;
}